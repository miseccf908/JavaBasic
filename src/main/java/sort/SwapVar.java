package sort;

public class SwapVar {/// 这个类不能用。
    public static void swapString(String a,String b){
        String temp = new String();
        temp=a;
        a=b;
        b=temp;
    }

    public static void swapInt(int a,int b){
        int temp = 0;
        temp=a;
        a=b;
        b=temp;
    }
}
